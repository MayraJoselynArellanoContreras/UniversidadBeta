    public void eliminar(){
        int fila = tablaPagos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this,
                "Seleccione un pago de la tabla para eliminar",
                "Selecci√≥n requerida",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String idPago = modeloTabla.getValueAt(fila, 0).toString();
        String garantia = modeloTabla.getValueAt(fila, 1).toString();
        String monto = modeloTabla.getValueAt(fila, 2).toString();
        
        int confirmacion = JOptionPane.showConfirmDialog(this,
            "¬øEst√° seguro de eliminar este pago?\n\n" +
            "ID: " + idPago + "\n" +
            "Garant√≠a: " + garantia + "\n" +
            "Monto: " + monto,
            "Confirmar eliminaci√≥n",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE);
        
        if (confirmacion == JOptionPane.YES_OPTION) {
            eliminarPago(idPago);
        }
    }
public void historial(){
    String idGarantia = JOptionPane.showInputDialog(this,
        "Ingrese ID de la garant√≠a para ver su historial:",
        "Historial por Garant√≠a",
        JOptionPane.QUESTION_MESSAGE);
    
    if (idGarantia == null || idGarantia.trim().isEmpty()) {
        return;
    }
    
    String sql = """
        SELECT 
            P.idPago,
            P.montoPago,
            CONVERT(VARCHAR, P.fechaPago, 23) as fechaPago,
            P.observaciones,
            D.nombre as donador,
            G.cantidadGarantizada as totalGarantia
        FROM Pago P
        JOIN Garantia G ON P.idGarantia = G.idGarantia
        JOIN Donador D ON G.idDonador = D.idDonador
        WHERE P.idGarantia = ?
        ORDER BY P.fechaPago
        """;
    
    try (Connection con = ConexionBD.getConexion();
         PreparedStatement ps = con.prepareStatement(sql)) {
        
        ps.setString(1, idGarantia.trim());
        ResultSet rs = ps.executeQuery();
        
        StringBuilder historial = new StringBuilder();
        historial.append("üìã HISTORIAL DE PAGOS - Garant√≠a #").append(idGarantia).append("\n\n");
        
        double totalPagado = 0;
        int numPagos = 0;
        double totalGarantia = 0;
        
        while (rs.next()) {
            numPagos++;
            double monto = rs.getDouble("montoPago");
            totalPagado += monto;
            
            // Obtener totalGarantia solo en la primera iteraci√≥n
            if (numPagos == 1) {
                totalGarantia = rs.getDouble("totalGarantia");
            }
            
            historial.append(numPagos).append(". ")
                    .append(rs.getString("fechaPago")).append(" - $")
                    .append(String.format("%,.2f", monto)).append("\n");
            
            if (rs.getString("observaciones") != null) {
                historial.append("   Obs: ").append(rs.getString("observaciones")).append("\n");
            }
        }
        
        if (numPagos == 0) {
            historial.append("No hay pagos registrados para esta garant√≠a.");
        } else {
            double saldo = totalGarantia - totalPagado;
            
            historial.append("\n").append("‚ïê".repeat(50)).append("\n");
            historial.append("RESUMEN:\n");
            historial.append("‚Ä¢ Total garant√≠a: $").append(String.format("%,.2f", totalGarantia)).append("\n");
            historial.append("‚Ä¢ Total pagado: $").append(String.format("%,.2f", totalPagado)).append("\n");
            historial.append("‚Ä¢ Saldo pendiente: $").append(String.format("%,.2f", saldo)).append("\n");
            historial.append("‚Ä¢ Porcentaje pagado: ").append(String.format("%.1f%%", (totalPagado / totalGarantia) * 100));
        }
        
        // Mostrar historial
        JTextArea textArea = new JTextArea(historial.toString(), 20, 50);
        textArea.setEditable(false);
        
        JScrollPane scrollPane = new JScrollPane(textArea);
        scrollPane.setPreferredSize(new java.awt.Dimension(600, 400));
        
        JOptionPane.showMessageDialog(this, scrollPane, 
            "Historial de Pagos - Garant√≠a #" + idGarantia, 
            JOptionPane.INFORMATION_MESSAGE);
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}
