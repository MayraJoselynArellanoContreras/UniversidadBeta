    private void configurarTabla() {
        String[] columnas = {
            "ID Donativo",
            "Fecha",
            "Monto",
            "Método Pago",
            "N° Pagos",
            "ID Donador",
            "Donador",
            "Corporación",
            "Círculo"
        };
        
        modeloTabla = new DefaultTableModel(columnas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        tablaDonativos.setModel(modeloTabla);
        tablaDonativos.setDefaultEditor(Object.class, null);
    }
    

    private void cargarTodos() {
        String sql = "SELECT * FROM v_DonativosDetalle ORDER BY fecha DESC";
        ejecutarConsulta(sql);
    }
    

    private void cargarPorDonador(int idDonador) {
        String sql = "SELECT * FROM v_DonativosDetalle WHERE idDonador = ? ORDER BY fecha DESC";
        
        try (Connection con = ConexionBD.getConexion();
             PreparedStatement ps = con.prepareStatement(sql)) {
            
            ps.setInt(1, idDonador);
            ResultSet rs = ps.executeQuery();
            llenarTabla(rs);
            
            setTitle("Donativos del Donador ID: " + idDonador);
            
        } catch (SQLException e) {
            manejarError(e);
        }
    }
    

    private void cargarPorDonativo(int idDonativo) {
        String sql = "SELECT * FROM v_DonativosDetalle WHERE idDonativo = ?";
        
        try (Connection con = ConexionBD.getConexion();
             PreparedStatement ps = con.prepareStatement(sql)) {
            
            ps.setInt(1, idDonativo);
            ResultSet rs = ps.executeQuery();
            llenarTabla(rs);
            
            setTitle("Detalle del Donativo ID: " + idDonativo);
            
        } catch (SQLException e) {
            manejarError(e);
        }
    }
    

    private void ejecutarConsulta(String sql) {
        try (Connection con = ConexionBD.getConexion();
             PreparedStatement ps = con.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {
            
            llenarTabla(rs);
            
        } catch (SQLException e) {
            manejarError(e);
        }
    }
    

    private void llenarTabla(ResultSet rs) throws SQLException {
        modeloTabla.setRowCount(0); 
        
        while (rs.next()) {
            Object[] fila = {
                rs.getInt("idDonativo"),
                rs.getDate("fecha"),
                rs.getDouble("monto"),
                rs.getString("metodoPago"),
                rs.getInt("numeroPagos"),
                rs.getInt("idDonador"),
                rs.getString("nombreDonador"),
                rs.getString("corporacion"),
                rs.getString("circuloDonador")
            };
            modeloTabla.addRow(fila);
        }
        
        // Actualizar título con cantidad de registros
        setTitle("Donativos Detalle - " + modeloTabla.getRowCount() + " registros");
    }
    
    private void manejarError(SQLException e) {
        JOptionPane.showMessageDialog(this,
            "❌ Error: " + e.getMessage(),
            "Error de base de datos",
            JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
