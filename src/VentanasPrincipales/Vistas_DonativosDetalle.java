    private void btnBuscarIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarIDActionPerformed
       String idTexto = txtBuscarId.getText().trim();
        
        if (idTexto.isEmpty()) {
            cargarTodos();
            return;
        }
        
        try {
            int id = Integer.parseInt(idTexto);
            
            // Preguntar si es ID de donativo o donador
            Object[] opciones = {"Donativo", "Donador"};
            int seleccion = JOptionPane.showOptionDialog(this,
                "¿Buscar por ID de qué?",
                "Tipo de búsqueda",
                JOptionPane.DEFAULT_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                opciones,
                opciones[0]);
            
            if (seleccion == 0) { // Donativo
                cargarPorDonativo(id);
            } else if (seleccion == 1) { // Donador
                cargarPorDonador(id);
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                "ID debe ser un número válido",
                "Error de formato",
                JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarIDActionPerformed

